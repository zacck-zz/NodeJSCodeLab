var age = 26;

var results;

//add
results = age + 6;
console.log('age + 6 = ', results);

//subtract
results = age - 1;
console.log('age - 1 = ', results);

//multiply
results = age * 3;
console.log('age * 3  = ', results);

//divide
results = age /4 ;
console.log('age / 4 = ', results);

//find the remainder
results = age % 5;
console.log('age % 5 = ', results);


//decrement
results--;
console.log('results--', results);

//increment
results++;
console.log('results++ ', results);
